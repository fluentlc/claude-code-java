package ai.claude.code.tool;

import ai.claude.code.capability.TodoManager;
import ai.claude.code.core.OpenAiClient;
import ai.claude.code.core.ToolHandler;
import com.google.gson.JsonArray;
import com.google.gson.JsonObject;

import java.util.ArrayList;
import java.util.LinkedHashMap;
import java.util.List;
import java.util.Map;

/**
 * Todo 工具提供者 — 封装 todo 工具，供模型管理任务列表。
 * Todo tool provider — wraps the todo tool for the model to manage its task list.
 *
 * 模型通过 todo 工具全量替换当前 todo 列表，并将验证结果或渲染结果作为 tool_result 返回。
 * The model uses the todo tool to fully replace the current todo list; validation errors
 * or the rendered list are returned as tool_result.
 *
 * Todo 工具的 schema 结构较复杂（嵌套对象数组），无法用 OpenAiClient.schema() 的
 * 三元组快捷方法生成，需手动构建 JsonObject。
 * The todo tool's schema has a complex nested structure (array of objects) that cannot
 * be generated by OpenAiClient.schema()'s triplet shorthand — it must be built manually.
 */
public class TodoTool implements ToolProvider {

    /** Todo 管理器实例，持有实际的列表状态 / TodoManager instance holding actual list state */
    private final TodoManager tm;

    public TodoTool(TodoManager tm) {
        this.tm = tm;
    }

    /**
     * 返回工具名称到处理器的映射，供 agentLoop 分派使用。
     * Returns the tool name to handler mapping for agentLoop dispatch.
     */
    public Map<String, ToolHandler> handlers() {
        Map<String, ToolHandler> map = new LinkedHashMap<String, ToolHandler>();

        // todo: 全量替换列表；返回错误信息或渲染结果
        // todo: full list replacement; returns error message or rendered list
        map.put("todo", new ToolHandler() {
            public String execute(JsonObject input) {
                JsonArray items = input.getAsJsonArray("items");
                String error = tm.update(items);
                if (error != null) {
                    return error;
                }
                return tm.render();
            }
        });

        return map;
    }

    /**
     * 返回 OpenAI 格式的工具定义列表，发送给模型。
     * Returns OpenAI-format tool definitions list sent to the model.
     *
     * schema 需要手动构建，因为 items 是对象数组，每个元素有 id/text/status 三个字段，
     * 这种嵌套结构超出了 OpenAiClient.schema() 三元组快捷方法的表达能力。
     * The schema must be built manually because items is an array of objects with
     * three fields each — this nesting is beyond the triplet shorthand's capability.
     */
    public List<JsonObject> definitions() {
        // 构建 items 数组中每个元素的 schema（有 id、text、status 三个 string 字段）
        // Build the schema for each element in the items array (id, text, status)
        JsonObject itemProperties = new JsonObject();

        JsonObject idProp = new JsonObject();
        idProp.addProperty("type", "string");
        itemProperties.add("id", idProp);

        JsonObject textProp = new JsonObject();
        textProp.addProperty("type", "string");
        itemProperties.add("text", textProp);

        JsonObject statusProp = new JsonObject();
        statusProp.addProperty("type", "string");
        itemProperties.add("status", statusProp);

        JsonObject itemSchema = new JsonObject();
        itemSchema.addProperty("type", "object");
        itemSchema.add("properties", itemProperties);

        // required 数组包含 id、text、status 三个字段
        // required array contains id, text, status
        JsonArray itemRequired = new JsonArray();
        itemRequired.add("id");
        itemRequired.add("text");
        itemRequired.add("status");
        itemSchema.add("required", itemRequired);

        // 构建顶层 items 数组属性
        // Build the top-level items array property
        JsonObject itemsArrayProp = new JsonObject();
        itemsArrayProp.addProperty("type", "array");
        itemsArrayProp.add("items", itemSchema);

        // 构建顶层 parameters schema
        // Build the top-level parameters schema
        JsonObject properties = new JsonObject();
        properties.add("items", itemsArrayProp);

        JsonArray required = new JsonArray();
        required.add("items");

        JsonObject parameters = new JsonObject();
        parameters.addProperty("type", "object");
        parameters.add("properties", properties);
        parameters.add("required", required);

        // 组装工具定义
        // Assemble the tool definition
        JsonObject def = OpenAiClient.toolDef(
                "todo",
                "Update your todo list. Pass the complete list of items each time (full replacement).",
                parameters);

        List<JsonObject> defs = new ArrayList<JsonObject>();
        defs.add(def);
        return defs;
    }
}
